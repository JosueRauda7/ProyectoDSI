package sv.edu.udb.www.beans;

import java.util.Date;

/**
 *
 * @author admi
 */
public class Pedido {
    private int idPedido;
    private String fechaCompra;
    private String montoTotal;
    private Usuario usuario;
    private int idUsuario;
    private EstadoCompra estadoCompra;
    private int idEstadoCompra;
    public Pedido(){
        this.idPedido=0;
        this.fechaCompra="";
        this.estadoCompra=null;
        this.montoTotal="";
        this.usuario=null;
        this.idUsuario=0;
        this.idEstadoCompra=0;
    }

    public int getIdPedido() {
        return idPedido;
    }

    public void setIdPedido(int idPedido) {
        this.idPedido = idPedido;
    }

    public String getFechaCompra() {
        return fechaCompra;
    }

    public void setFechaCompra(String fechaCompra) {
        this.fechaCompra = fechaCompra;
    }

    public String getMontoTotal() {
        return montoTotal;
    }

    public void setMontoTotal(String montoTotal) {
        this.montoTotal = montoTotal;
    }

    public Usuario getUsuario() {
        return usuario;
    }

    public void setUsuario(Usuario usuario) {
        this.usuario = usuario;
    }

    public EstadoCompra getEstadoCompra() {
        return estadoCompra;
    }

    public void setEstadoCompra(EstadoCompra estadoCompra) {
        this.estadoCompra = estadoCompra;
    }

    public int getIdUsuario() {
        return idUsuario;
    }

    public void setIdUsuario(int idUsuario) {
        this.idUsuario = idUsuario;
    }

    public int getIdEstadoCompra() {
        return idEstadoCompra;
    }

    public void setIdEstadoCompra(int idEstadoCompra) {
        this.idEstadoCompra = idEstadoCompra;
    }
    
}
